def get_text(key, lang="ja", **kwargs):
    """
    指定された言語で文字列を返す。
    見つからない場合は日本語、それもなければキー自体を返す。
    """
    lang_map = TRANSLATIONS.get(lang, TRANSLATIONS["ja"])
    text = lang_map.get(key, TRANSLATIONS["ja"].get(key, key))
    
    if kwargs:
        try:
            return text.format(**kwargs)
        except:
            return text
    return text

